// Head-to-head player slots with a centered VS divider. When the opponent
// slot is empty (user is alone), we render a pulsing pending circle as
// the obvious "share your code" affordance.
// Memoised so the 1-second idle-warning tick doesn't keep re-mounting the
// emote bubble keys and replaying their float animations.
const PlayersVs = memo(_PlayersVs);
function _PlayersVs({
  me,
  opponent,
  hostClientId,
  emotes,
}: {
  me: PresenceMember | null;
  opponent: PresenceMember | null;
  hostClientId: string | undefined;
  emotes: Record<
    string,
    { primaryUrl: string; fallbackUrl: string; label: string; loopMs: number; key: number }
  >;
}) {
  return (
    <div className="grid grid-cols-[1fr_auto_1fr] items-center gap-2 sm:gap-4">
      {me ? (
        <PlayerSlot
          member={me}
          isHost={me.clientId === hostClientId}
          isSelf
          emote={emotes[me.clientId] ?? null}
          anchor="left"
        />
      ) : (
        <EmptySlot label="You" side="left" />
      )}

      {/* VS centerpiece — gradient-clip in the site's spring→aqua accent */}
      <div className="flex flex-col items-center select-none px-1">
        <span className="hidden sm:block h-px w-10 bg-gradient-to-r from-transparent via-spring-green-500/30 to-transparent mb-3" />
        <span
          className={`${orbitron.className} text-4xl sm:text-5xl font-black tracking-tight bg-gradient-to-br from-spring-green-300 to-aquamarine-400 bg-clip-text text-transparent`}
          aria-hidden
        >
          VS
        </span>
        <span className="hidden sm:block h-px w-10 bg-gradient-to-r from-transparent via-spring-green-500/30 to-transparent mt-3" />
      </div>

      {opponent ? (
        <PlayerSlot
          member={opponent}
          isHost={opponent.clientId === hostClientId}
          isSelf={false}
          emote={emotes[opponent.clientId] ?? null}
          anchor="right"
        />
      ) : (
        <EmptySlot label="Waiting…" side="right" />
      )}
    </div>
  );
}

