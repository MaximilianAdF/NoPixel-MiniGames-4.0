// Lobby lifetime — universal across all games.
const LOBBY_IDLE_MS = 5 * 60 * 1000;   // close lobby after 5 min of no matches
const LOBBY_WARN_MS = 60 * 1000;       // show warning toast last 60s
const MATCH_MAX_MS = 3 * 60 * 1000;    // cap any single match at 3 min → draw
const COUNTDOWN_MS = 3 * 1000;         // sync the start: both clients unblock at the same goAt

// Fire-and-forget durable counter (lib/lobby/stats). Independent of GA4/GTM
// so 1v1 usage is queryable from Mongo regardless of tag-manager config.
type LobbyStatEvent =
  | 'match_started' | 'match_completed' | 'match_forfeited'
  | 'match_timed_out' | 'lobby_created';
function recordLobbyStat(event: LobbyStatEvent, game?: GameType): void {
  void fetch('/api/lobby/stats', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ event, game }),
  }).catch(() => {});
}

interface LobbyClientProps {
  code: string;
}

export default function LobbyClient({ code }: LobbyClientProps) {
  const { user } = useUser();
  const router = useRouter();
  // Anonymous play: guests get a stable per-tab identity stored in
  // sessionStorage so a reload keeps them in the same lobby slot.
  const [guestId, setGuestId] = useState<string | null>(null);
  useEffect(() => {
    if (typeof window === 'undefined') return;
    const stored = sessionStorage.getItem('lobbyGuestId');
    if (stored) {
      setGuestId(stored);
      return;
    }
    const fresh = `guest-${(typeof crypto !== 'undefined' && crypto.randomUUID
      ? crypto.randomUUID()
      : Math.random().toString(36).slice(2)
    ).slice(0, 8)}`;
    sessionStorage.setItem('lobbyGuestId', fresh);
    setGuestId(fresh);
  }, []);
  const effectiveClientId = user?.id ?? guestId;
  const effectiveDisplayName = user?.displayName
    ?? user?.username
    ?? (guestId ? `Guest ${guestId.slice(-4).toUpperCase()}` : 'Player');
  const [copied, setCopied] = useState(false);
  const [match, setMatch] = useState<{ game: GameType; seed: number } | null>(null);
  const [myResult, setMyResult] = useState<GameResult | null>(null);
  const [opponentResult, setOpponentResult] = useState<GameResult | null>(null);
  const [opponentInputs, setOpponentInputs] = useState<unknown[]>([]);
  const [matchStartTime, setMatchStartTime] = useState<number | null>(null);
  // My own timestamped input stream for the current match. The winner posts
  // this as a ghost (it has accurate per-input timing; the host only sees the
  // opponent's network-jittered stream). Reset at the start of every match.
  const myInputLogRef = useRef<{ t: number; input: unknown }[]>([]);
  const matchStartTimeRef = useRef<number | null>(null);
  useEffect(() => {
    matchStartTimeRef.current = matchStartTime;
  }, [matchStartTime]);
  const [matchExpired, setMatchExpired] = useState(false);
  const [lobbyLastActivity, setLobbyLastActivity] = useState(() => Date.now());
  const [now, setNow] = useState(() => Date.now());
  // Synced go-time in the host's Date.now() frame, shipped on match:start.
  // Both clients compare to their own Date.now() — same wall-clock fire if
  // their clocks are NTP-synced (typical for browsers). Display is driven by
  // per-second setTimeouts aligned to goAt so the digits tick exactly at
  // second boundaries instead of waiting on a stale 1Hz interval.
  const [goAt, setGoAt] = useState<number | null>(null);
  // Host-controlled per-lobby setting. Persists across matches in the same lobby.
  const [focusMode, setFocusMode] = useState(false);
  // Each match captures the focusMode the host had at start time, so a mid-match
  // toggle doesn't affect the in-progress game.
  const [matchFocusMode, setMatchFocusMode] = useState(false);
  // Canonical outcome — host derives once it has full info and broadcasts via
  // match:outcome; both ends render based on this state, no per-client flicker.
  const [outcome, setOutcome] = useState<{
    winnerClientId: string | null;
    reason: 'finished' | 'timeout';
  } | null>(null);
  // Recent emote per player — wiped after the emote's loopMs window so
  // the WebP doesn't visibly restart and the bubble fades on cue.
  const [emotes, setEmotes] = useState<
    Record<
      string,
      { primaryUrl: string; fallbackUrl: string; label: string; loopMs: number; key: number }
    >
  >({});
  const lastEmoteSentRef = useRef(0);
  // Which game (if any) the host is publishing to the public lobby browser.
  // Null = private; setting a GameType heartbeats the lobby and lists it
  // under that game. Picking the same game again clears it (unpublishes).
  const [publishGame, setPublishGame] = useState<GameType | null>(null);
  // Last few matches played in this lobby (in-memory; resets on reload).
  const [matchHistory, setMatchHistory] = useState<MatchHistoryEntry[]>([]);

  // Preload lobby audio cues once on mount.
  useEffect(() => {
    checkBeepPlayer.whenReady();
    successPlayer.whenReady();
  }, []);

  const showEmote = useCallback((emoteId: string, fromClientId: string) => {
    const def = EMOTE_BY_ID[emoteId];
    if (!def) return;
    const key = Date.now();
    setEmotes((prev) => ({
      ...prev,
      [fromClientId]: {
        // Triggered emote is always rendered animated; if the animated WebP
        // 404s for some reason we fall back to the Fluent 3D PNG.
        primaryUrl: def.animatedUrl,
        fallbackUrl: def.fallbackUrl,
        label: def.label,
        loopMs: def.loopMs,
        key,
      },
    }));
    // Sound for both sender (immediate gesture) and receiver. The shared
    // AudioContext is created lazily and resumes on first user interaction,
    // so a receiver who's already clicked anywhere will hear it.
    playEmoteSound(emoteId);
    setTimeout(() => {
      setEmotes((prev) => {
        if (prev[fromClientId]?.key !== key) return prev;
        const next = { ...prev };
        delete next[fromClientId];
        return next;
      });
    }, def.loopMs);
  }, []);

  const handleMessage = useCallback((msg: LobbyMessage) => {
    if (msg.type === 'match:start') {
      setMatch({ game: msg.game, seed: msg.seed });
      setMyResult(null);
      setOpponentResult(null);
      setOpponentInputs([]);
      myInputLogRef.current = [];
      setMatchExpired(false);
      setMatchFocusMode(msg.focusMode);
      setOutcome(null);
      // Use the host's goAt. Cap to a sane local range so a wildly-skewed
      // peer clock can't make the countdown last minutes or fire instantly.
      const receiveNow = Date.now();
      const cappedGoAt = Math.min(
        Math.max(msg.goAt, receiveNow + 500),
        receiveNow + COUNTDOWN_MS + 1500,
      );
      // Sync `now` atomically so the first countdown render isn't off by
      // however stale the 1Hz interval state happened to be.
      setNow(receiveNow);
      setGoAt(cappedGoAt);
      // Match timer starts counting from GO, not from when match:start arrived.
      setMatchStartTime(cappedGoAt);
    } else if (msg.type === 'match:result') {
      setOpponentResult({ won: msg.won, score: msg.score, elapsedMs: msg.elapsedMs });
    } else if (msg.type === 'match:input') {
      setOpponentInputs((prev) => [...prev, msg.input]);
    } else if (msg.type === 'match:timeout') {
      setMatchExpired(true);
    } else if (msg.type === 'match:outcome') {
      setOutcome({ winnerClientId: msg.winnerClientId, reason: msg.reason });
    } else if (msg.type === 'emote') {
      showEmote(msg.emote, msg.fromClientId);
    }
    // 'suggest' messages from older clients are ignored — the guest no
    // longer has a suggest picker. Host's game pick is the single source
    // of truth.
  }, [showEmote]);

  const { status, presence, publish } = useAblyChannel<LobbyMessage>({
    channelName: `lobby:${code}`,
    presenceData: {
      displayName: effectiveDisplayName,
      discordId: user?.discordId,
      avatarHash: user?.avatar,
    },
    onMessage: handleMessage,
    enabled: !!effectiveClientId,
    clientId: effectiveClientId ?? undefined,
  });

  const hostClientId = determineHost(presence);
  const isHost = hostClientId !== undefined && hostClientId === effectiveClientId;
  const me = presence.find((p) => p.clientId === effectiveClientId) ?? null;
  const opponent = presence.find((p) => p.clientId !== effectiveClientId) ?? null;
  const opponentClientId = opponent?.clientId ?? null;

  // 1Hz ticker — drives all the countdown displays + expiry checks below.
  useEffect(() => {
    const interval = setInterval(() => setNow(Date.now()), 1000);
    return () => clearInterval(interval);
  }, []);

  // Only a new joiner counts as activity. If the opponent gets idle-kicked
  // first, the host's presence drops 2→1; we don't want that to reset the
  // host's own idle clock and let them sit forever.
  // Plays a subtle audio cue when the opponent first joins so the host
  // notices without having to watch the lobby tab.
  const prevPresenceLenRef = useRef(0);
  useEffect(() => {
    if (presence.length > prevPresenceLenRef.current) {
      setLobbyLastActivity(Date.now());
      if (prevPresenceLenRef.current > 0) {
        // Skip on initial 0→1 (that's just us mounting); fire when a peer joins.
        checkBeepPlayer.play();
      }
    }
    prevPresenceLenRef.current = presence.length;
  }, [presence.length]);

  // Audio: success sound when an opponent's result arrives (they finished
  // their game) so we know to look up. Skips on every other state change.
  const prevOpponentResultRef = useRef<GameResult | null>(null);
  useEffect(() => {
    if (opponentResult && !prevOpponentResultRef.current) {
      checkBeepPlayer.play();
    }
    prevOpponentResultRef.current = opponentResult;
  }, [opponentResult]);

  // Audio: success chime exactly at GO when the match unblocks.
  useEffect(() => {
    if (!goAt) return;
    const delay = goAt - Date.now();
    if (delay <= 0) {
      successPlayer.play();
      return;
    }
    const handle = setTimeout(() => successPlayer.play(), delay);
    return () => clearTimeout(handle);
  }, [goAt]);

  // When a match ends (back to lobby), reset activity so the next idle window
  // starts fresh.
  useEffect(() => {
    if (!match && !matchStartTime) return;
    if (match && (myResult || opponentResult || matchExpired)) {
      setLobbyLastActivity(Date.now());
    }
  }, [match, matchStartTime, myResult, opponentResult, matchExpired]);

  // Idle-lobby timeout: schedule a setTimeout aligned to the deadline rather
  // than polling `now` each tick. Polling could quietly miss firing if the
  // 1Hz interval was throttled (background tab) or out of phase; setTimeout
  // fires once at the right moment. Skipped only while a match is actively
  // running — the outcome view and pre-match countdown count toward idle.
  const isInActiveMatch = !!(match && !myResult && !opponentResult && !matchExpired);
  useEffect(() => {
    if (isInActiveMatch) return;
    const delay = lobbyLastActivity + LOBBY_IDLE_MS - Date.now();
    if (delay <= 0) {
      router.push('/lobby');
      return;
    }
    const handle = setTimeout(() => router.push('/lobby'), delay);
    return () => clearTimeout(handle);
  }, [isInActiveMatch, lobbyLastActivity, router]);

  // Schedule a re-render at each second boundary of the countdown, all
  // anchored on goAt. Without this the 1Hz interval fires at whatever phase
  // it landed on at lobby-mount, so the "1" digit might only flash for a
  // few hundred ms before the match starts.
  useEffect(() => {
    if (!goAt) return;
    const handles: ReturnType<typeof setTimeout>[] = [];
    for (let target = goAt - 2000; target <= goAt; target += 1000) {
      const delay = Math.max(0, target - Date.now());
      handles.push(setTimeout(() => setNow(Date.now()), delay));
    }
    return () => handles.forEach(clearTimeout);
  }, [goAt]);

  // Match max-duration: also setTimeout-based so it can't be missed by a
  // throttled tick. First client to expire publishes match:timeout so both
  // converge on a draw without UI flicker. Guards prevent firing after the
  // match is already resolved (e.g., opponent forfeited at 2:30 and the
  // timer would otherwise still flip the outcome to a fake draw at 3:00).
  useEffect(() => {
    if (!match || matchStartTime === null || matchExpired) return;
    if (myResult || opponentResult) return;
    const fire = () => {
      setMatchExpired(true);
      trackMatchTimedOut({ lobby_code: code, game_type: match.game });
      void publish({ type: 'match:timeout' });
    };
    const delay = matchStartTime + MATCH_MAX_MS - Date.now();
    if (delay <= 0) {
      fire();
      return;
    }
    const handle = setTimeout(fire, delay);
    return () => clearTimeout(handle);
  }, [match, matchStartTime, matchExpired, myResult, opponentResult, code, publish]);

  // Host arbiter: derive and broadcast the canonical match:outcome once we
  // have enough info. If only one side has a result yet, wait briefly for
  // the other before locking in.
  useEffect(() => {
    if (!isHost || outcome || !effectiveClientId || !opponentClientId) return;
    if (!myResult && !opponentResult && !matchExpired) return;

    const decide = () =>
      deriveWinner(myResult, opponentResult, matchExpired, effectiveClientId, opponentClientId);

    const publishAndSet = (decision: ReturnType<typeof deriveWinner>) => {
      void publish({
        type: 'match:outcome',
        winnerClientId: decision.winnerClientId,
        reason: decision.reason,
      });
      setOutcome(decision);
      if (match) {
        trackMatchCompleted({
          lobby_code: code,
          game_type: match.game,
          reason: decision.reason,
          has_winner: decision.winnerClientId !== null,
          duration_ms: matchStartTime ? Date.now() - matchStartTime : 0,
        });
      }
    };

    // Both results in hand (or matchExpired) — decide immediately.
    if ((myResult && opponentResult) || matchExpired) {
      publishAndSet(decide());
      return;
    }

    // Only one side reported. Give the other ~600ms to land before locking.
    const handle = setTimeout(() => publishAndSet(decide()), 600);
    return () => clearTimeout(handle);
  }, [isHost, myResult, opponentResult, matchExpired, outcome, effectiveClientId, opponentClientId, publish, code, match, matchStartTime]);

  // Append to match history once the canonical outcome lands, deduped by
  // match.seed so multiple effect runs don't push the same row twice.
  useEffect(() => {
    if (!outcome || !match) return;
    setMatchHistory((prev) => {
      if (prev.some((e) => e.seed === match.seed)) return prev;
      const winner = outcome.winnerClientId
        ? presence.find((p) => p.clientId === outcome.winnerClientId)
        : null;
      const entry: MatchHistoryEntry = {
        seed: match.seed,
        game: match.game,
        winnerClientId: outcome.winnerClientId,
        winnerName: winner?.data.displayName,
        winnerDiscordId: winner?.data.discordId,
        winnerAvatarHash: winner?.data.avatarHash,
        durationMs: matchStartTime ? Date.now() - matchStartTime : 0,
        endedAt: Date.now(),
      };
      return [entry, ...prev].slice(0, 5);
    });
  }, [outcome, match, presence, matchStartTime]);

  // Persist the finished match — once per seed, host only — to:
  //   1. recentMatches  → the public "live activity" ticker on /lobby
  //   2. lobbyStats      → the durable all-time counter (source of truth)
  // Both are best-effort and fire from the host so each match counts once.
  const recordedSeedsRef = useRef<Set<number>>(new Set());
  useEffect(() => {
    if (!outcome || !match || !isHost) return;
    if (recordedSeedsRef.current.has(match.seed)) return;
    recordedSeedsRef.current.add(match.seed);

    const winner = outcome.winnerClientId
      ? presence.find((p) => p.clientId === outcome.winnerClientId)
      : null;
    const loser = outcome.winnerClientId
      ? presence.find((p) => p.clientId !== outcome.winnerClientId)
      : null;

    void fetch('/api/lobby/recent-matches', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        game: match.game,
        winnerName: winner?.data.displayName ?? null,
        winnerDiscordId: winner?.data.discordId,
        winnerAvatarHash: winner?.data.avatarHash,
        opponentName: loser?.data.displayName ?? null,
        durationMs: matchStartTime ? Date.now() - matchStartTime : 0,
      }),
    }).catch(() => {});

    void recordLobbyStat(
      outcome.reason === 'timeout' ? 'match_timed_out' : 'match_completed',
      match.game,
    );
  }, [outcome, match, isHost, presence, matchStartTime]);

  // Harvest a ghost — winner-only, recorded by the WINNER's own client (it
  // holds the accurately-timed input log). Conditions: I won, the match
  // actually finished (not a timeout/forfeit), my result says won, and I have
  // a sane number of recorded inputs. Deduped per seed. Best-effort; this is
  // what fills the async-race pool. See docs/async-ghost-spec.md.
  const ghostRecordedSeedsRef = useRef<Set<number>>(new Set());
  useEffect(() => {
    if (!outcome || !match || !effectiveClientId) return;
    // Only harvest from games whose replay is verified for ghost racing.
    if (!isGhostEnabled(match.game)) return;
    if (ghostRecordedSeedsRef.current.has(match.seed)) return;
    const iWon = outcome.winnerClientId === effectiveClientId;
    if (!iWon || outcome.reason !== 'finished') return;
    if (!myResult?.won) return; // my own engine confirms a genuine completion
    const inputs = myInputLogRef.current;
    if (inputs.length < 2) return; // too few moves — skip degenerate runs
    ghostRecordedSeedsRef.current.add(match.seed);

    const me = presence.find((p) => p.clientId === effectiveClientId);
    void fetch('/api/lobby/ghosts', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        game: match.game,
        seed: match.seed,
        inputs,
        result: myResult,
        recorderName: me?.data.displayName ?? effectiveDisplayName,
        recorderDiscordId: me?.data.discordId,
        recorderAvatarHash: me?.data.avatarHash,
        recorderClientId: effectiveClientId,
      }),
    }).catch(() => {});
  }, [outcome, match, effectiveClientId, myResult, presence, effectiveDisplayName]);

  // Non-host fallback: if the host's match:outcome doesn't arrive within a
  // couple of seconds (host disconnected mid-match), derive locally so the
  // outcome view still renders.
  useEffect(() => {
    if (isHost || outcome || !effectiveClientId || !opponentClientId) return;
    if (!myResult && !opponentResult && !matchExpired) return;

    const handle = setTimeout(() => {
      setOutcome(deriveWinner(myResult, opponentResult, matchExpired, effectiveClientId, opponentClientId));
    }, 2500);
    return () => clearTimeout(handle);
  }, [isHost, myResult, opponentResult, matchExpired, outcome, effectiveClientId, opponentClientId]);

  // Auto-resolve: if the opponent disconnects mid-match (closed tab, network
  // drop with no quick reconnect), give the win to the remaining player after
  // a short grace window. Covers brief flickers without ending early.
  useEffect(() => {
    if (!match || outcome || !effectiveClientId) return;
    if (presence.length >= 2) return;
    const handle = setTimeout(() => {
      const decision = { winnerClientId: effectiveClientId, reason: 'finished' as const };
      void publish({
        type: 'match:outcome',
        winnerClientId: decision.winnerClientId,
        reason: decision.reason,
      });
      setOutcome(decision);
      if (match) {
        trackMatchCompleted({
          lobby_code: code,
          game_type: match.game,
          reason: 'finished',
          has_winner: true,
          duration_ms: matchStartTime ? Date.now() - matchStartTime : 0,
        });
      }
    }, 3500);
    return () => clearTimeout(handle);
  }, [match, outcome, presence.length, effectiveClientId, publish, code, matchStartTime]);

  // 3rd-player kick: presence dedupes by clientId, but a public lobby could
  // race two joiners filling it past 2. Sort by Ably timestamp so every
  // client agrees on who arrived first; if I'm the 3rd, bounce back to the
  // lobby browser.
  useEffect(() => {
    if (presence.length < 3 || !effectiveClientId) return;
    const sorted = [...presence].sort((a, b) => a.timestamp - b.timestamp);
    const myIdx = sorted.findIndex((p) => p.clientId === effectiveClientId);
    if (myIdx >= 2) {
      router.replace('/lobby?full=1');
    }
  }, [presence, effectiveClientId, router]);

  // Public lobby heartbeat. Only the host pings — and only when logged in,
  // since the API requires a real session. The lobby is "published" iff
  // the host has picked a publishGame; that pick is the game label the
  // listing shows + filters by. The DB entry expires 90s after the last
  // ping so closing the tab / clearing publishGame frees the slot.
  const isLoggedInHost = isHost && !!user?.id;
  useEffect(() => {
    if (!publishGame || !isLoggedInHost) return;
    let cancelled = false;
    const ping = async () => {
      try {
        await fetch('/api/lobby/public', {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            code,
            playerCount: presence.length,
            suggestedGame: publishGame,
          }),
        });
      } catch {
        // Heartbeat is best-effort — a missed ping just shortens the
        // listing's TTL by one cycle.
      }
    };
    void ping();
    // 15s heartbeat (TTL is 45s) so the listing stays alive while the host
    // is here but expires quickly once they leave.
    const interval = setInterval(() => {
      if (!cancelled) void ping();
    }, 15_000);
    return () => {
      cancelled = true;
      clearInterval(interval);
    };
  }, [publishGame, isLoggedInHost, code, presence.length]);

  // Explicit unregister when host clears publishGame or unmounts. The TTL
  // would clean up eventually, but firing the DELETE removes the listing
  // immediately so other browsers see the change on next refresh.
  const wasPublishedRef = useRef(false);
  // Mirror the live host/code into refs so the unmount cleanup below (which
  // runs with an empty dep array) sees the *current* values, not the ones
  // captured at mount — at mount isLoggedInHost is still false, so without
  // this a host who closes the tab while public never unlists the lobby and
  // it lingers in the pool the full TTL.
  const isLoggedInHostRef = useRef(isLoggedInHost);
  const codeRef = useRef(code);
  useEffect(() => {
    isLoggedInHostRef.current = isLoggedInHost;
    codeRef.current = code;
  }, [isLoggedInHost, code]);

  useEffect(() => {
    if (publishGame) {
      wasPublishedRef.current = true;
      return;
    }
    if (!wasPublishedRef.current || !isLoggedInHost) return;
    wasPublishedRef.current = false;
    void fetch(`/api/lobby/public/${code}`, { method: 'DELETE' }).catch(() => {});
  }, [publishGame, isLoggedInHost, code]);
  useEffect(() => {
    return () => {
      if (wasPublishedRef.current && isLoggedInHostRef.current) {
        void fetch(`/api/lobby/public/${codeRef.current}`, { method: 'DELETE' }).catch(() => {});
      }
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);


  const handleCopy = async () => {
    if (typeof window === 'undefined') return;
    try {
      // Copy the full invite URL (not just the code) so a paste is a
      // one-click join for whoever receives it.
      await navigator.clipboard.writeText(window.location.href);
      setCopied(true);
      setTimeout(() => setCopied(false), 1500);
    } catch {
      // Silent fail.
    }
  };


  const handleStart = async (game: GameType) => {
    const seed = generateMatchSeed();
    const startNow = Date.now();
    const newGoAt = startNow + COUNTDOWN_MS;
    setMatch({ game, seed });
    setMyResult(null);
    setOpponentResult(null);
    setOpponentInputs([]);
    myInputLogRef.current = [];
    setMatchExpired(false);
    setMatchFocusMode(focusMode);
    setOutcome(null);
    // Sync `now` atomically with goAt so the initial countdown render shows
    // exactly 3 instead of 4 (the 1Hz interval state can be up to 1s stale).
    setNow(startNow);
    setGoAt(newGoAt);
    setMatchStartTime(newGoAt);
    trackMatchStarted({ lobby_code: code, game_type: game, focus_mode: focusMode });
    recordLobbyStat('match_started', game);
    await publish({
      type: 'match:start',
      game,
      seed,
      startedAt: startNow,
      goAt: newGoAt,
      focusMode,
    });
  };

  const handleMatchEnd = useCallback(
    (result: GameResult) => {
      setMyResult(result);
      void publish({
        type: 'match:result',
        won: result.won,
        score: result.score,
        elapsedMs: result.elapsedMs,
      });
    },
    [publish],
  );

  const handleInput = useCallback(
    (input: unknown) => {
      // Log my own input with a timestamp relative to match start, so if I win
      // this run can be replayed as a ghost at its true cadence (capped at
      // MAX so a pathological run can't grow unbounded).
      const start = matchStartTimeRef.current;
      if (start !== null && myInputLogRef.current.length < 5000) {
        myInputLogRef.current.push({ t: Math.max(0, Date.now() - start), input });
      }
      void publish({ type: 'match:input', input });
    },
    [publish],
  );

  const handleForfeit = useCallback(() => {
    if (myResult) return;
    const elapsedMs = matchStartTime ? Date.now() - matchStartTime : 0;
    const result: GameResult = { won: false, score: 0, elapsedMs };
    setMyResult(result);
    if (match) {
      trackMatchForfeited({ lobby_code: code, game_type: match.game, duration_ms: elapsedMs });
    }
    void publish({ type: 'match:result', won: false, score: 0, elapsedMs });
  }, [myResult, matchStartTime, match, code, publish]);

  const handleBackToLobby = () => {
    setMatch(null);
    setMyResult(null);
    setOpponentResult(null);
    setOpponentInputs([]);
    setMatchExpired(false);
    setMatchStartTime(null);
    setGoAt(null);
    setOutcome(null);
    setLobbyLastActivity(Date.now());
  };

  const sendEmote = useCallback(
    (emoteId: string) => {
      const nowMs = Date.now();
      if (nowMs - lastEmoteSentRef.current < EMOTE_THROTTLE_MS) return;
      if (!effectiveClientId) return;
      lastEmoteSentRef.current = nowMs;
      // Show on my own side immediately (echo is disabled — we won't get our
      // own message back). showEmote also plays the sound.
      showEmote(emoteId, effectiveClientId);
      void publish({ type: 'emote', emote: emoteId, fromClientId: effectiveClientId });
    },
    [effectiveClientId, publish, showEmote],
  );

  // Synced countdown: rendered before anything else so neither client can
  // start playing before goAt.
  if (match && goAt && now < goAt) {
    return <Countdown remainingMs={goAt - now} />;
  }

  // Outcome view: only when the canonical outcome is in. Until then (between
  // a partial result landing and the host's match:outcome arriving) we keep
  // the game on-screen so the user isn't briefly shown the wrong winner.
  if (match && outcome) {
    const iWon = outcome.winnerClientId === effectiveClientId;
    const isDraw = outcome.winnerClientId === null;
    return (
      <>
        <OutcomeView
          iWon={iWon}
          isDraw={isDraw}
          reason={outcome.reason}
          game={match.game}
          winnerClientId={outcome.winnerClientId}
          myResult={myResult}
          opponentResult={opponentResult}
          me={me}
          opponent={opponent}
          emotes={emotes}
          onBack={handleBackToLobby}
        />
        {presence.length >= 2 && (
          <div className="fixed bottom-4 left-1/2 -translate-x-1/2 z-40">
            <EmoteBar onSend={sendEmote} />
          </div>
        )}
      </>
    );
  }

  // Match in progress.
  if (match) {
    const matchRemainingMs =
      matchStartTime !== null ? Math.max(0, matchStartTime + MATCH_MAX_MS - now) : MATCH_MAX_MS;
    return (
      <>
        <MatchHeader remainingMs={matchRemainingMs} onForfeit={handleForfeit} />
        <MatchView
          game={match.game}
          seed={match.seed}
          onMatchEnd={handleMatchEnd}
          onInput={handleInput}
          opponentInputs={opponentInputs}
          focusMode={matchFocusMode}
          me={me}
          opponent={opponent}
          emotes={emotes}
        />
        <div className="fixed bottom-4 left-1/2 -translate-x-1/2 z-40">
          <EmoteBar onSend={sendEmote} />
        </div>
      </>
    );
  }

  // Lobby view.
  const lobbyRemainingMs = Math.max(0, lobbyLastActivity + LOBBY_IDLE_MS - now);
  const showIdleWarning = lobbyRemainingMs <= LOBBY_WARN_MS;
  const meMember = presence.find((p) => p.clientId === effectiveClientId) ?? null;
  const opponentMember = presence.find((p) => p.clientId !== effectiveClientId) ?? null;

  return (
    <main className="relative min-h-screen flex flex-col bg-gradient-to-br from-mirage-950 via-mirage-900 to-mirage-950">
      <LobbyBackdrop />

      <div className="relative z-10 w-full max-w-3xl mx-auto px-4 sm:px-6 pt-6 pb-32">
        {/* Top utility row — 3-column grid so the code chip is dead-centre
            regardless of how wide the Leave / status sides are. */}
        <div className="grid grid-cols-3 items-center gap-3 mb-12">
          <div className="justify-self-start">
            <Link
              href="/lobby"
              className="group inline-flex items-center gap-2 text-gray-400 hover:text-spring-green-300 text-sm font-medium transition-colors"
            >
              <ArrowLeft className="w-4 h-4 group-hover:-translate-x-1 transition-transform" />
              Leave
            </Link>
          </div>

          <div className="justify-self-center">
            <CodeChip code={code} copied={copied} onCopy={handleCopy} />
          </div>

          <div className="justify-self-end">
            <ConnectionStatus status={status} />
          </div>
        </div>

        {/* Players — the head-to-head hero */}
        <section className="mb-12">
          {status === 'failed' ? (
            <div className="rounded-lg bg-error-500/[0.06] border border-error-500/30 px-5 py-6 text-center">
              <p className="text-error-300 text-sm font-medium mb-1">
                Couldn&apos;t connect to the lobby
              </p>
              <p className="text-gray-400 text-xs mb-4">
                The realtime connection failed. Check your network and try again.
              </p>
              <button
                type="button"
                onClick={() => window.location.reload()}
                className="inline-flex items-center gap-1.5 rounded-lg bg-white/[0.04] hover:bg-white/[0.08] border border-white/10 hover:border-spring-green-400/40 text-white/90 hover:text-spring-green-300 px-4 py-2 text-sm font-semibold transition-all"
              >
                Retry
              </button>
            </div>
          ) : presence.length === 0 ? (
            <div className="py-12 text-center">
              <Loader2 className="w-5 h-5 animate-spin text-spring-green-400/40 mx-auto mb-3" />
              <p className="text-gray-500 text-xs font-mono">connecting to lobby…</p>
            </div>
          ) : (
            <PlayersVs
              me={meMember}
              opponent={opponentMember}
              hostClientId={hostClientId}
              emotes={emotes}
            />
          )}
        </section>

        {/* Host gets the game picker; the guest waits. */}
        {isHost ? (
          <GamePicker
            isAlone={presence.length < 2}
            publishGame={publishGame}
            onPublishChange={setPublishGame}
            onStart={handleStart}
            focusMode={focusMode}
            onFocusModeChange={setFocusMode}
            loggedIn={!!user?.id}
          />
        ) : presence.length >= 2 ? (
          <div className="rounded-lg bg-mirage-900/40 border border-white/[0.08] px-5 py-5 flex items-center gap-3">
            <Loader2 className="w-4 h-4 text-spring-green-400/50 animate-spin shrink-0" />
            <div className="text-gray-300 text-sm">
              Waiting for the host to pick a game…
            </div>
          </div>
        ) : null}

        {matchHistory.length > 0 && (
          <section className="mt-8">
            <div className="flex items-center gap-3 mb-4">
              <h2 className="text-sm font-bold bg-gradient-to-r from-spring-green-400 to-aquamarine-400 bg-clip-text text-transparent">
                Recent matches
              </h2>
              <span className="flex-1 h-px bg-gradient-to-r from-spring-green-500/20 to-transparent" />
            </div>
            <ul className="space-y-1.5">
              {matchHistory.map((entry) => (
                <MatchHistoryRow key={entry.seed} entry={entry} />
              ))}
            </ul>
          </section>
        )}
      </div>

      {/* Floating emote bar — visible in the lobby and outcome views. */}
      {presence.length >= 2 && (
        <div className="fixed bottom-4 left-1/2 -translate-x-1/2 z-40">
          <EmoteBar onSend={sendEmote} />
        </div>
      )}

      {showIdleWarning && (
        <div className="fixed bottom-24 left-1/2 -translate-x-1/2 z-50 px-4 py-2.5 rounded-full bg-amber-500/15 backdrop-blur-md border border-amber-500/35 text-amber-200 text-xs shadow-2xl shadow-amber-500/10 inline-flex items-center gap-2 tabular-nums">
          <Loader2 className="w-3.5 h-3.5 animate-spin" />
          Lobby closes in {Math.ceil(lobbyRemainingMs / 1000)}s
        </div>
      )}
    </main>
  );
}

